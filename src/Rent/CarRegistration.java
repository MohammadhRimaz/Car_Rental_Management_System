    public void table_update()
    {
        int c;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mr rentals","root","");
            pst = con.prepareStatement("select * from carregistration");
            ResultSet Rs = pst.executeQuery();
            ResultSetMetaData rd = Rs.getMetaData();
            c = rd.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            
            while(Rs.next())
            {
                Vector v2 = new Vector();
                
                for (int i=1;i<=c;i++)
                {
                    v2.add(Rs.getString("Car_ID"));
                    v2.add(Rs.getString("Car_Reg_No"));
                    v2.add(Rs.getString("Model"));
                    v2.add(Rs.getString("Car_Owner"));                
                    v2.add(Rs.getString("Contact_No"));
                    v2.add(Rs.getString("Available"));
                }
               df.addRow(v2);
            }
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CarRegistration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CarRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void autoID ()
    {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mr rentals","root","");
            Statement s = con.createStatement();
            
            ResultSet rs = s.executeQuery("select Max(Car_ID) from carregistration");
            rs.next();
            rs.getString("Max(Car_ID)");
            
            if (rs.getString("Max(Car_ID)")== null)
            {
                Car_id.setText("C0001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("Max(Car_ID)").substring(2,rs.getString("Max(Car_ID)").length()));
                id++;
                Car_id.setText("C0" + String.format("%03d",id));
            }  
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CarRegistration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CarRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
